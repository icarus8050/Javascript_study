// 2. Variables 
// let (added in ES6 에서 추가된 let)
//  Block Scope
// constant 값이 할당되면 바뀌지 않는 값. 
// Dynamic typing;
let text = 'hello';
// value : hello | type: string 
text = 1; 
// value : 1 | type: number 
text = "7" + 5;
// value: 75; | type: string 으로 나옴
text = "8" / "2";
// value : 4 | type: number! 연산자를 썼기 때문?
